package com.example.moobie.Jualan;

public class PreviewStrukItem {

    String text1, text2, text3;
    Integer text4;

    public PreviewStrukItem(String text1,
                      String text2,
                            String text3,
                      Integer text4
                      ) {
        this.text1 = text1;
        this.text2 = text2;
        this.text3 = text3;
        this.text4 = text4;

    }

    public String getText1() {
        return text1;
    }
    public String getText2() {
        return text2;
    }
    public String getText3() {
        return text3;
    }
    public Integer getText4() {
        return text4;
    }

}


